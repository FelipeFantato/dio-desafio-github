package org.example;

public class Calculadora {

    public static int calcularSoma(int n1, int n2){
        return n1 + n2;

    }
    public static int calcularSub(int n1, int n2){
        return n1 - n2;
    }
    public static float calcularDiv(int n1, int n2){
        return n1 / n2;
    }
    public static int calcularMulti(int n1, int n2){
        return n1 * n2;
    }

}
